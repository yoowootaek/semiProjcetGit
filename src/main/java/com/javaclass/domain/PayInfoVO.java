package com.javaclass.domain;

public class PayInfoVO {
	int pay_number;
    String pay_name;
    String pay_select;
    String pay_email;
    int pay_price;
    String pay_date;
    
    
	public int getPay_number() {
		return pay_number;
	}
	public void setPay_number(int pay_number) {
		this.pay_number = pay_number;
	}
	public String getPay_name() {
		return pay_name;
	}
	public void setPay_name(String pay_name) {
		this.pay_name = pay_name;
	}
	public String getPay_select() {
		return pay_select;
	}
	public void setPay_select(String pay_select) {
		this.pay_select = pay_select;
	}
	public String getPay_email() {
		return pay_email;
	}
	public void setPay_email(String pay_email) {
		this.pay_email = pay_email;
	}
	public int getPay_price() {
		return pay_price;
	}
	public void setPay_price(int pay_price) {
		this.pay_price = pay_price;
	}
	public String getPay_date() {
		return pay_date;
	}
	public void setPay_date(String pay_date) {
		this.pay_date = pay_date;
	}
    
    
    
    

}
